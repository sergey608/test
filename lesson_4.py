# Определение функции track_chars, которая принимает на вход строку
def track_chars(input_str):
    # Разделение входной строки на список символов с помощью функции split()
    input_list = input_str.split()
    # Инициализация пустого списка для хранения выходных данных
    output_list = []
    # Инициализация словаря для подсчета повторений каждого символа
    count_dict = {}

    # Цикл по каждому символу в списке
    for char in input_list:
        # Если символ уже встречался ранее
        if char in count_dict:
            # Увеличиваем счетчик для этого символа
            count_dict[char] += 1
            # Добавляем символ с постфиксом в формате _n в выходной список
            output_list.append(f"{char}_{count_dict[char]}")
        else:
            # Если символ встречается впервые, добавляем его в словарь со значением 0
            count_dict[char] = 0
            # Добавляем символ без постфикса в выходной список
            output_list.append(char)

    # Возвращаем строку, состоящую из символов с постфиксами, разделенных пробелами
    return ' '.join(output_list)

# Пример использования функции
input_str = "a a a b c a a d c d d"
print(track_chars(input_str))  # a a_1 a_2 b c a_3 a_4 d c_1 d_1 d_2
