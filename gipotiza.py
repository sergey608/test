for a in range(1, 151):
    for b in range(a, 151):  # начинаем с a, чтобы избежать повторений
        for c in range(b, 151):  # начинаем с b, чтобы избежать повторений
            for d in range(c, 151):  # начинаем с c, чтобы избежать повторений
                e = (a**5 + b**5 + c**5 + d**5) ** 0.2  # вычисляем e
                if e == int(e) and e <= 150:  # проверяем, что e - целое число и не превышает 150
                    print(f"a={a}, b={b}, c={c}, d={d}, e={int(e)}, сумма={a+b+c+d+int(e)}")
